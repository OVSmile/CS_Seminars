// ------------------------------Начало программы------------------------------------------------
Console.Clear();
int X1, Y1, X2, Y2;
X1 = Koords("Введите координату Х1: "); // Вызов метода с передачей параметров 
Y1 = Koords("Введите координату Y1: ");
X2 = Koords("Введите координату Х2: ");
Y2 = Koords("Введите координату Y2: ");

Console.WriteLine($"Расстояние, между точкой А({X1}, {Y1}) и точкой В({X2}, {Y2}), равно {PrintDistanceBetweenPoints(X1, Y1, X2, Y2)}");
