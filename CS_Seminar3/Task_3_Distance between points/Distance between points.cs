// Определяем функцию, выполняющую ввод значений координат
static int Koords(string massedg)
{
    try
    {
        Console.Write(massedg);
        int result = int.Parse(Console.ReadLine() ?? "");
        return result;
    }
    catch (Exception exc)
    {
        Console.WriteLine($"Ошибка ввода данных! {exc.Message}");
        return -1;
    }
}   

// ---------------------------------------------------------------------------------------------
// Определяем функцию, принимающую значение координаты двух точек
// и выводящую в консоль рассояник между точеками (х и у) d = √ (х А – х В) 2 + (у А – у В) 2
static double PrintDistanceBetweenPoints(int x1, int y1, int x2, int y2)
{
    double result = Math.Sqrt(Math.Pow((x2 - x1), 2) + Math.Pow((y2 - y1), 2));
    return result;
}

